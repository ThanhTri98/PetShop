package com.example.petmarket2020.Models;

public class CoinsModel {
    private long coinId;
    private long value;

    public CoinsModel() {
    }

    public long getCoinId() {
        return coinId;
    }

    public void setCoinId(long coinId) {
        this.coinId = coinId;
    }

    public long getValue() {
        return value;
    }

    public void setValue(long value) {
        this.value = value;
    }
}
